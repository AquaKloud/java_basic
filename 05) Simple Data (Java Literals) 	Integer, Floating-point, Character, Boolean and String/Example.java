public class Example {
    public static void main(String []args) {
        System.out.println(1234); //Integer Literals
        System.out.println(-1234); //Integer Literals
        
        System.out.println(1.234); //Floating-point Literals
        System.out.println(0.0012); //Floating-point Literals
        System.out.println(-0.0012); //Floating-point Literals
        
        System.out.println('A'); //Character Literals
        System.out.println('7'); //Character Literals
        
        System.out.println(true); //Boolean Literals
        System.out.println(false); //Boolean Literals
        
        System.out.println("Niroth"); //String Literals
        System.out.println("A"); //String Literals
        System.out.println("7"); //String Literals
    }
}

